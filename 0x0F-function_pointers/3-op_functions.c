#include "3-calc.h"

/**
 * op_add -  returns the sum of a and b.
 * @a: first input.
 * @b: second input.
 *
 * Return: result of sum.
 */

int op_add(int a, int b)
{
	return ((a) + (b));
}

/**
 * op_sub -  returns the sub of a and b.
 * @a: first input.
 * @b: second input.
 *
 * Return: result of sub.
 */

int op_sub(int a, int b)
{
	return ((a) - (b));
}

/**
 * op_mul -  returns the mul of a and b.
 * @a: first input.
 * @b: second input.
 *
 * Return: result of mul.
 */

int op_mul(int a, int b)
{
	return ((a) * (b));
}

/**
 * op_div -  returns the div of a and b.
 * @a: first input.
 * @b: second input.
 *
 * Return: result of div.
 */

int op_div(int a, int b)
{
	return ((a) / (b));
}

/**
 * op_mod -  returns the mod of a and b.
 * @a: first input.
 * @b: second input.
 *
 * Return: result of mod.
 */

int op_mod(int a, int b)
{
	return ((a) % (b));
}
